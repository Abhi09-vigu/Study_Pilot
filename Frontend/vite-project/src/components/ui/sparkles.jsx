/* use client */
import React, { useId, useEffect, useState } from 'react';
import Particles, { initParticlesEngine } from '@tsparticles/react';
import { loadSlim } from '@tsparticles/slim';
import { motion, useAnimation } from 'motion/react';
import { cn } from '@/lib/utils';

export function SparklesCore({ id, className, background, minSize, maxSize, speed, particleColor, particleDensity }) {
  const [init, setInit] = useState(false);

  useEffect(() => {
    initParticlesEngine(async (engine) => {
      await loadSlim(engine);
    }).then(() => setInit(true));
  }, []);

  const controls = useAnimation();
  const generatedId = useId();

  async function particlesLoaded(container) {
    if (container) {
      controls.start({ opacity: 1, transition: { duration: 1 } });
    }
  }

  return (
    <motion.div animate={controls} className={cn('opacity-0', className)}>
      {init && (
        <Particles
          id={id || generatedId}
          className={cn('h-full w-full')}
          particlesLoaded={particlesLoaded}
          options={{
            background: { color: { value: background || 'transparent' } },
            fullScreen: { enable: false, zIndex: 1 },
            fpsLimit: 120,
            interactivity: {
              events: { onClick: { enable: false, mode: 'push' }, onHover: { enable: false, mode: 'repulse' }, resize: true },
              modes: { push: { quantity: 2 }, repulse: { distance: 200, duration: 0.4 } },
            },
            particles: {
              color: { value: particleColor || '#ffffff' },
              move: { direction: 'none', enable: true, speed: { min: 0.1, max: 1 }, random: false, straight: false, outModes: { default: 'out' } },
              number: { density: { enable: true, width: 400, height: 400 }, value: particleDensity || 300 },
              opacity: { value: { min: 0.1, max: 1 }, animation: { enable: true, speed: speed || 2, sync: false, startValue: 'random' } },
              shape: { type: 'circle' },
              size: { value: { min: minSize || 0.6, max: maxSize || 1.4 } },
            },
            detectRetina: true,
          }}
        />
      )}
    </motion.div>
  );
}
